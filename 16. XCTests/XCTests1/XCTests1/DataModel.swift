import Foundation

struct Calculator {
    func sum(_ n1: Int, _ n2: Int) -> Int {
        return n1+n2
    }

    func difference(_ n1: Int, _ n2: Int) -> Int {
        return n1-n2
    }

    func product(_ n1: Int, _ n2: Int) -> Int {
        return n1*n2
    }

    func division(_ n1: Int, _ n2: Int) -> Int {
        return n1/n2
    }
}
